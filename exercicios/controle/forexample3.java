package exercicios.controle;

public class forexample3 {
    public static void main(String[] args) {

        for (int x = 1; x <= 5; x++) {
            for (int y = 1; y <= 5; y++) {
                System.out.printf("(%d, %d) ", x, y);
            }
            System.out.println();
        }
    }
}

